package controlstmts;

public class Nestedforloop {

	public static void main(String[] args) {
		// Nestedforloop
		for(int i=1;i<=3;i++)//outer loop - row details
		{
			for(int j=1;j<=4;j++)//inner loop -column details
			{
				System.out.print("*");
			}
			System.out.println();
		}

		
		
	}

}
